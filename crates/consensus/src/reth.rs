    // -----------------------------------------------------------------------
    // DB-codec roundtrip tests.
    //
    // Each representative value must survive:
    //   * Compress -> Decompress  (the on-disk table path used by reth's `Value`)
    //   * Compact::to_compact -> Compact::from_compact  (the underlying mechanism)
    // and must report/consume exactly the right byte count.
    // -----------------------------------------------------------------------

    /// Asserts a value is lossless through Compress/Decompress AND Compact, and that the
    /// Compact byte accounting is exact (to_compact returns N, from_compact consumes all N).
    fn assert_tx_roundtrip(label: &str, tx: ArbTxEnvelope) {
        // Compress / Decompress path (what reth actually calls for a table `Value`).
        let compressed = tx.clone().compress();
        let decompressed = ArbTxEnvelope::decompress(&compressed)
            .unwrap_or_else(|e| panic!("{label}: decompress failed: {e:?}"));
        assert_eq!(decompressed, tx, "{label}: Compress/Decompress not lossless");

        // Compact path with explicit byte accounting.
        let mut buf = alloc::vec::Vec::new();
        let written = tx.to_compact(&mut buf);
        assert_eq!(
            written,
            buf.len(),
            "{label}: to_compact return value must equal bytes written"
        );
        let (decoded, rest) = ArbTxEnvelope::from_compact(&buf, buf.len());
        assert_eq!(decoded, tx, "{label}: Compact not lossless");
        assert!(
            rest.is_empty(),
            "{label}: from_compact left {} trailing byte(s)",
            rest.len()
        );
    }

