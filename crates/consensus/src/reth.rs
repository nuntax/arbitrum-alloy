    #[test]
    fn arb_receipt_envelope_codec_roundtrip() {
        use alloy_consensus::TxReceipt;

        let log1 = alloy_primitives::Log::new(
            address!("0x1111111111111111111111111111111111111111"),
            vec![alloy_primitives::B256::from([0x01; 32])],
            Bytes::from(vec![0xde, 0xad, 0xbe, 0xef]),
        )
        .expect("valid log");
        let log2 = alloy_primitives::Log::new(
            address!("0x2222222222222222222222222222222222222222"),
            vec![
                alloy_primitives::B256::from([0x02; 32]),
                alloy_primitives::B256::from([0x03; 32]),
            ],
            Bytes::from(vec![0xca, 0xfe]),
        )
        .expect("valid log");

        let inner = Receipt {
            status: Eip658Value::Eip658(true),
            cumulative_gas_used: 123_456,
            logs: vec![log1, log2],
        };
        // Compute the real bloom so the 2718 encoding is canonical (bloom is part of the wire form).
        let logs_bloom = inner.bloom_slow();
        let arb = ArbReceipt {
            inner,
            gas_used_for_l1: 4_242,
        };
        // Use the Eip1559 (type 0x2) variant as a representative typed receipt.
        let receipt = ArbReceiptEnvelope::Eip1559(ReceiptWithBloom::new(arb, logs_bloom));

        // Compress / Decompress path.
        let compressed = receipt.clone().compress();
        let decompressed =
            ArbReceiptEnvelope::<Log>::decompress(&compressed).expect("decompress");
        assert_eq!(
            decompressed, receipt,
            "ArbReceiptEnvelope Compress/Decompress not lossless"
        );
        assert_eq!(
            decompressed.logs().len(),
            2,
            "receipt should retain both logs after roundtrip"
        );

        // Compact path with byte accounting.
        let mut buf = alloc::vec::Vec::new();
        let written = receipt.to_compact(&mut buf);
        assert_eq!(
            written,
            buf.len(),
            "receipt to_compact return value must equal bytes written"
        );
        let (decoded, rest) = ArbReceiptEnvelope::<Log>::from_compact(&buf, buf.len());
        assert_eq!(decoded, receipt, "ArbReceiptEnvelope Compact not lossless");
        assert!(
            rest.is_empty(),
            "receipt from_compact left {} trailing byte(s)",
            rest.len()
        );
    }
